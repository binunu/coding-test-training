# 2개 이하로 다른 비트 level2
def solution(numbers):
    answer = []
    for num in numbers:
        if num % 2 == 0:
            answer.append(num+1)
        else:
            s = bin(num)
            i = s.rfind('01')
            if i == -1:
                s = s[0:2] + '10' + s[3:]
            else:
                s = s[:i] + '10' + s[i+2:]
            
            answer.append(int(s,2))
        
    return answer

